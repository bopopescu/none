谷精草
释名：戴星草、文星草、流星草。
气味：（花）辛、温、无毒。
主治：
1、脑痛、眉痛。用谷精草二钱、地在三钱、乳香一钱，共研为末。每用半钱，烧烟筒中，熏鼻。
2、偏正头痛。用谷精草，研为末，加白面糊调匀搜摊纸上贴痛处，干了变换。又方：用谷精草末、铜绿各一钱，硝石半分，混匀，随头痛的左、右边，吸入左右鼻孔中。
3、鼻血不止。用谷精草为末，每服二钱，熟面汤送下。
4、目中翳膜。用谷精草、防风，等分为末，米汤冲服，甚验。
5、小儿雀盲（夜盲）。用羊肺（按：张绍棠味古斋版本是用羊肝）一具，原物不洗，竹刀剖开，放入谷精草一撮，瓦罐煮熟，每天吃一些，有效。忌用铁器。如不肯吃，可炙熟后捣烂做成丸了，如绿豆大。每服三十丸，茶送下。
附方：谷精划的订功用是疏散风热、明目退翳。李时珍指出，丹治各种眼病，加用本品，效果好，明目退翳之功，似在菊花之上也。


