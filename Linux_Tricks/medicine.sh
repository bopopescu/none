牛
释名：
气味：黄牛肉：甘、温、无毒。牛乳：甘、微寒、无毒。牛脑：甘、温、微毒。角胎：苦、温、无毒。牛尿：苦、辛、微温、无毒。牛屎：苦、寒、无毒。
主治：黄牛肉：牛乳：
1、风热毒气。用煎牛乳一升、生牛乳一升，和匀，空心服。一天服三次。
2、下虚消渴（心脾有热，下焦焦虚冷，小便多）。常喝牛乳或羊乳，每饮三、四合。黄牛肉：安中益气，养脾胃，补益腰脚，止消渴及唾涎。水牛肉：安中益气，养脾办。健强筋骨，消水肿，除湿气。牛脑：
1、吐血咯血，五劳七伤。用水牛脑一具，涂纸上阴干，杏仁（煮去皮）、胡桃仁、白蜜各一斤，香油四两，同熬干为末。每服二匙，空心服，烧酒送下。
2、偏正头刚巧。用白芷、川芎各三钱，研为细末，以黄牛脑粘末，加酒煮熟，乘热吃下。酒醉无妨，醒则其病如失。甚效。
3、脾积痞病。用黄牛脑一具，去皮筋，捣烂；另用皮硝末一斤，蒸饼六个，晒干，研细，与牛脑和匀，加粗做成丸子，如梧子大。每服三十丸，空心服，好酒送下。一天服三次，服至百日，见效。此方名“牛脑丸”。
4、气积成块。用牛脑一具（去皮筋）、雄鸡肫一个（连里黄皮），一起放酒中浸中宿，捣烂，加入木香、沉香、砂仁各三两，皮硝一碗，共捣匀，在铜锅内以文武火烘干为末。再加轻粉三钱调交。每服二钱，空心服，烧酒送下。一天服三次。角胎（好年听坚骨）：
1、大肠冷痢。用牛角胎烧灰，每服二钱，水送下。一天服二次。
2、大便下血。用黄年角胎一具，煅为末，同豉煮汁。每服二钱，一天服三次。
3、赤白带。用牛角胎烧至烟断。附子以盐水浸七次（去皮），等分为末。每服二匙，空心服，酒送下。牛尿：水肿、腹胀、脚满，利不便。牛屎（亦牛洞）：
1、水肿溲涩。用黄牛屎一升，绞汁饮服。以小便通畅为见效。忌食盐。
2、湿热黄病。用典牛屎晒干，研为末，加面糊成丸子，如梧子大。每服七十丸，饭前服，开水送下。
3、霍乱吐泻，四肢发冷。用黄牛屎半升，加水二升，煮开三次，取半升饮服。
4、脚跟肿痛，不能着地。用牛屎加盐炒热包痛处。
5、妊娠腰痛。用牛屎烧成末，每服一匙。一天服三次。
6、小儿烂疮。用牛屎烧成灰封涂。兼灭瘢痕。
7、痈肿不俣。用牛屎烧成末，调鸡蛋白封含沙射影。药干即换，有特效。
8、乳痈初起。用牛屎和酒敷涂即消。
9、背疮溃烂。用陈年牛屎晒干，研为末，加百草霜调匀敷患处。



鳖
释名：团鱼、神守。
气味：鳖甲：咸、平、无毒。肉：甘、平、无毒。
主治：鳖甲：
1、老疟劳疟。用鳖甲醋炙、研末，每服一匙，酒送下。隔夜一服，清早一服，病发时一服，加雄黄少许更有效。
2、奔豚气痛，上冲心胜利。用鳖甲（醋炙）三两、亦三棱（煨）二两、桃仁（去皮尖）四两，加醋煎如糖浆，以瓶收存，每服半匙，空心服，酒送下。
3、血瘕症癖（按：即肿瘤之类的病）。用鳖甲、琥珀、在黄，等分为末，酒送服二钱。不久即恶血排下。又方：用鳖甲以醛炙黄，研为末，每服一匙，牛乳一俣调下。每晨一次。
4、妇女漏下。用鳖甲（醋炙）研为末，清酒送服一匙。一天服二次，又方：用干姜、鳖甲、诃黎勒皮，等分为末，加糊做成丸子。每倾家荡产心服三十丸。一天服二次。
5、妇女难产。用鳖甲烧存性，研为末。酒送服一匙，即可产下。
6、小儿诸疾。用鳖甲炙过，研为末。每服一钱，乳汁关下。一天服二次，亦可加蜜做成丸子服用。
7、突然腰痛，不可俯仰。用鳖甲炙过，研为末，每服一匙，酒送下。一天服二次。
8、沙石洒。用鳖甲醋炙过，研为末。每服一匙，酒送一。一天服三次，石出即愈。
9、阴虚梦泄。用鳖甲烧过，研为末。每用三分，和酒半碗、童尿半碗、葱白七寸同煎，去葱，下午饮服。
10、吐血不止。用鳖甲、蛤粉各一两，同炒至色黄，加熟地黄一两半，晒干，共研为末，每服二钱，饭后服茶送下。
11、痈疽敛。用鳖甲烧存性研为末，掺敷患处。肉；1痃癖气块。以蚕沙一斗、桑柴灰一斗，水淋五次取汁，煮在鳖一个如泥，去骨，再煮成膏，捣为丸，如梧子大，每服十丸，一天服三次。
2、寒湿脚气，痛不可忍。用鳖二个，加水二斗，煮成一斗，去鳖取汁，再加苍耳、苍术、寻风藤各半斤，煎至七升，去渣，乘热熏患处。待药水转温，再将患处浸洗。
3、肺结核。用鳖一个，柴胡、前胡、贝母、知母、杏仁各五钱，同煮熟。去骨、甲、裙煮汁和丸，如梧了大。生前了三十丸，空心服，黄芪汤送下。一天服二次。此方名“团鱼丸”。


