茜草
释名：茅搜、茹芦、地血、染绯草、血见悉、风车草、过山龙、牛蔓。
气味：（根）苦、寒、无毒。
主治：
1、吐血。用茜根一两，捣成末。每服二钱，水煎，冷眼，用水调末二钱服亦可。
2、妇女经闭。用茜根一两，煎酒服。
3、盅毒（吐血、下血如猪肝）用茜草根、蓑荷叶各三分，加水四升，煮成二升服。
4、脱肛。用茜根、石榴皮各一把，加酒一碗，煎至七成，温服。


