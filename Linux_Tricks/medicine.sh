灯心草
释名：亦名虎须草、碧玉草。
气味：（茎、根）甘、寒、无毒。
主治：
1、伤口流血。用灯心草嚼烂敷患处。
2、鼻血不止。用灯心草一两为末，加丹砂一钱。每服二钱，米汤送下。
3、喉痹。用灯心草一把，瓦上烧存性，加炒盐一匙，每取少许吹入喉中。又方：用灯心草灰二钱，加硼砂粉一钱，和匀，吹喉。又方：用灯心草、箬叶烧灰，等分和匀，吹喉。又方：用灯心草、红花烧灰，酒送服一钱。
4、失眠。用灯心草煎水代茶喝。
5、湿热黄疸。用灯草根四两，加酒、水各半，煮半日，露一夜，温服。


